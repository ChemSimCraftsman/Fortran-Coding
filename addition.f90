program addition
    implicit none
    real :: num1, num2, sum

    ! Prompt the user for the first number
    print *, "Enter the first number:"
    read *, num1

    ! Prompt the user for the second number
    print *, "Enter the second number:"
    read *, num2

    ! Calculate the sum
    sum = num1 + num2

    ! Display the result
    print *, "The sum is:", sum
end program addition

